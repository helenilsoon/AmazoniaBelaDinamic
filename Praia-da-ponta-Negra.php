					<h2>Praia da Ponta Negra </h2>
						<p>A Praia da Ponta Negra, é uma praia de bairro homônimo localizada à margem esquerda do rio Negro, a aproximadamente 13 km do centro de Manaus. É um dos principais cartões postais da cidade.[1]</p>
						<p>Possui orla urbanizada, quadras para prática de esporte e um anfiteatro onde são realizadas apresentações musicais, espetáculos teatrais e outras atrações, tornando-a um dos principais pontos turísticos de Manaus.[2]</p>
						<p>Com uma decoração muito nobre, o Teatro Amazonas já foi palco não somente de grandes peças teatrais, mas também de shows internacionais como o da banda The White Stripes.[11] Desde 1997, o Festival Amazonas de Ópera é realizado no teatro.[12][13] Em 2008, o teatro foi eleito uma das sete maravilhas brasileiras em dois concursos promovidos pela Revista Caras em parceria com o banco HSBC e outro pelo escritório de design Goff.[14]</p>
						
